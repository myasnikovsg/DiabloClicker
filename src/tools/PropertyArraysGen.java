	public static int[] wrap(int prop[]){
		boolean group_visited[] = new boolean[7];
		int group_n = 0;
		for (int i = 0; i < prop.length; i++) {
			if (!group_visited[property_group[prop[i]]]) {
				group_visited[property_group[prop[i]]] = true;
				group_n++;
			}
		}
		int res[] = new int[prop.length + group_n];
		int count = 0;
		for (int i = 0; i < 7; i++) 
			if (group_visited[i]) {
				res[count++] = -(i + 1);
				for (int j = 0; j < prop.length; j++)
					if (property_group[prop[j]] == i) 
						res[count++] = prop[j];
			}
		return res;
	}
	
